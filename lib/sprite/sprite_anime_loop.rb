class Sprite_AnimeLoop < Sprite_Anime
  #--------------------------------------------------------------------------
  # ● 更新当前帧位置
  #--------------------------------------------------------------------------
  def update_frame
    if @frame == @max_frame - 1 then @frame = 0
      else @frame += 1
    end
  end
end