#==============================================================================
# ■ Sprite_Number
#------------------------------------------------------------------------------
#  显示数字的精灵类，在 Spriteset_Number 内部使用
#==============================================================================

class Sprite_Number < Sprite_M5
  #--------------------------------------------------------------------------
  # ● 初始化
  #--------------------------------------------------------------------------
  def initialize(viewport = nil, bitmap = Bitmap.new(1,1))
    super(viewport)
    self.bitmap = bitmap
    @width = self.bitmap.width / 10
    clear
  end
  #--------------------------------------------------------------------------
  # ● 隐藏
  #--------------------------------------------------------------------------
  def clear
    self.visible = false
  end
  #--------------------------------------------------------------------------
  # ● 显示数字
  #--------------------------------------------------------------------------
  def show(num)
    self.src_rect.set(num * @width, 0, @width, self.bitmap.height)
    self.visible = true
  end
  #--------------------------------------------------------------------------
  # ● 不释放来自调用者的位图
  #--------------------------------------------------------------------------
  def dispose_bitmap
  end
end