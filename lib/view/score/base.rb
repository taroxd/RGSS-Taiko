# encoding: utf-8
require 'view/number'

# 显示当前分数

module View
  class Score
    class Base < Number
      #--------------------------------------------------------------------------
      # ● 获取位图
      #--------------------------------------------------------------------------
      def get_bitmap
        Cache.score_numbers('font_m')
      end
      #--------------------------------------------------------------------------
      # ● 数字 X 坐标
      #--------------------------------------------------------------------------
      def pos_x; Skin_Setting.setting(:ScoreX); end
      #--------------------------------------------------------------------------
      # ● 数字 Y 坐标
      #--------------------------------------------------------------------------
      def pos_y; Skin_Setting.setting(:ScoreY); end
      #--------------------------------------------------------------------------
      # ● 数字 Z 高度
      #--------------------------------------------------------------------------
      def pos_z; 0; end
      #--------------------------------------------------------------------------
      # ● 数字间距的调整
      #--------------------------------------------------------------------------
      def pos_width; 0; end
      #--------------------------------------------------------------------------
      # ● 居右对齐
      #--------------------------------------------------------------------------
      def pos_type; 2; end
    end
  end
end